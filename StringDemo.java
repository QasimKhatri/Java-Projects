// A simple program demonstrating String objects.

public class StringDemo
{
   public static void main(String[] args)
   {
      String greeting = "Good afternoon, ";
      String name = "Qasim";
      String str1 = "Programming is great fun.";
      String str2 = " I can't get enough of it!";
   
      
      System.out.println(greeting + name);
      System.out.println(str1 + str2);
      System.out.println("\nProgramming is great fun.\nI can't get enough of it!");
      System.out.println("\nProgramming is great fun.\n\tI can't get enough of it!");
      
   }
   
	   
		    
   
}